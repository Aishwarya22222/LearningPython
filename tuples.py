# tuple -> similar to list but immutable that is element inside tuple cannot be changed

fruits=('banana','mango','lichhi','apple','orange','watermelon','apple')
print(fruits)
print(fruits[5])

#len
print(len(fruits)) 

# basic tuple methods
# .index() to enter the element and return the index number
print(fruits.index('apple'))

# .count() to count the number of times the element is appear in the tuple
print(fruits.count('apple'))
print(fruits.count('mango'))



